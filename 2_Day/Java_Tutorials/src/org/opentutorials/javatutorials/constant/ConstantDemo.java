package org.opentutorials.javatutorials.constant;

public class ConstantDemo {

	public static void main(String[] args) {

		//정수형 상수는 int형이다. (단, byte와 short는 자체적으로 처리..)
		//실수형 상수는 double형이다.
		
		long a = 2147483648L; //뒤에 L을 붙인다.
		
		byte b = 100;		
		short c = 200;
		
		float d = 2.2F; //뒤에 F을 붙인다.
		
		//참고 https://blog.naver.com/tnsehf12345/220528424386
		
	}

}
