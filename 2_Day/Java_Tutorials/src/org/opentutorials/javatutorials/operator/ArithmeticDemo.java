package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {

	public static void main(String[] args) {

		int result = 1 + 2;
		System.out.println(result);
		
		result = result - 1 ;
		System.out.println(result);
		
		result = result + 1 ;
		System.out.println(result);

		result = result * 2 ;
		System.out.println(result);
		
		result = result / 5 ;
		System.out.println(result);
		
	}

}
